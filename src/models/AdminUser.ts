import mongoose from 'mongoose'

const adminUserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ['admin', 'editor', 'viewer'],
    default: 'viewer'
  },
  isActive: {
    type: Boolean,
    default: true
  },
  lastLogin: Date,
  profileImage: String
}, {
  timestamps: true
})

export default mongoose.models.AdminUser || mongoose.model('AdminUser', adminUserSchema)
